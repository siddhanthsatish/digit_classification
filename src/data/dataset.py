"""
SVHN Format 1 dataset loader.
Parses digitStruct.mat to extract per-digit bounding boxes from full scene images.
Each cropped digit is resized to 32x32 and labelled 0-9 (SVHN label 10 → 0).

Class 10 (background) crops are pre-generated by scripts/generate_bg_crops.py
and loaded from manifests at data/bg_crops/train_manifest.json and test_manifest.json.
"""

import os
import json
import pickle
import random
import cv2
import h5py
import numpy as np
from PIL import Image
from torch.utils.data import Dataset, DataLoader
from torchvision import transforms
from tqdm import tqdm

from src.pipeline.nms import iou, is_encompassing

BG_LABEL = 10   # class index for background / non-digit


# ── digitStruct.mat parser ────────────────────────────────────────────────────

def _read_attr(f, obj, key):
    val = obj[key]
    if val.shape == (1, 1):
        return [int(val[0][0])]
    return [int(f[val[j][0]][0][0]) for j in range(val.shape[0])]


def parse_digit_struct(mat_path):
    cache_path = os.path.join(
        os.path.dirname(os.path.dirname(mat_path)), "cache",
        os.path.basename(os.path.dirname(mat_path)) + "_records.pkl"
    )
    if os.path.exists(cache_path):
        with open(cache_path, "rb") as f:
            return pickle.load(f)

    records = []
    with h5py.File(mat_path, "r") as f:
        ds        = f["digitStruct"]
        name_refs = ds["name"][:].flatten()
        bbox_refs = ds["bbox"][:].flatten()
        for name_ref, bbox_ref in tqdm(zip(name_refs, bbox_refs), total=len(name_refs),
                                       desc=f"Parsing {os.path.basename(os.path.dirname(mat_path))}/digitStruct.mat",
                                       unit="img"):
            name     = "".join(chr(c) for c in f[name_ref][:].flatten())
            bbox_obj = f[bbox_ref]
            records.append({
                "filename": name,
                "labels":   _read_attr(f, bbox_obj, "label"),
                "tops":     _read_attr(f, bbox_obj, "top"),
                "lefts":    _read_attr(f, bbox_obj, "left"),
                "heights":  _read_attr(f, bbox_obj, "height"),
                "widths":   _read_attr(f, bbox_obj, "width"),
            })

    os.makedirs(os.path.dirname(cache_path), exist_ok=True)
    with open(cache_path, "wb") as f:
        pickle.dump(records, f, protocol=pickle.HIGHEST_PROTOCOL)
    print(f"  Cached → {cache_path}")
    return records


# ── HNM helpers (used by generate_bg_crops.py) ───────────────────────────────

def _gt_boxes(rec):
    return [
        (int(left), int(top), int(w), int(h))
        for top, left, h, w in zip(
            rec["tops"], rec["lefts"], rec["heights"], rec["widths"]
        )
    ]


def _proposal_overlaps_gt(box, gt_boxes, iou_threshold):
    return any(iou(box, gt) >= iou_threshold for gt in gt_boxes)


def _filter_encompassing(proposals):
    kept = []
    for box in proposals:
        if not any(is_encompassing(box, k) for k in kept):
            kept.append(box)
    return kept


# ── Dataset ───────────────────────────────────────────────────────────────────

class SVHNFormat1Dataset(Dataset):
    """
    Crops individual digits from SVHN Format 1 full-scene images.
    Background crops loaded from pre-generated manifest (run scripts/generate_bg_crops.py first).
    """

    def __init__(self, split_dir, transform=None, bg_manifest=None, bg_ratio=1.0, seed=42):
        self.transform      = transform
        self._digit_samples = []
        self._bg_paths      = []

        mat_path = os.path.join(split_dir, "digitStruct.mat")
        records = parse_digit_struct(mat_path)

        for rec in records:
            img_path = os.path.join(split_dir, rec["filename"])
            if not os.path.exists(img_path):
                continue
            for label, top, left, h, w in zip(
                rec["labels"], rec["tops"], rec["lefts"],
                rec["heights"], rec["widths"]
            ):
                label = 0 if label == 10 else label
                self._digit_samples.append((img_path, label, top, left, h, w))

        if bg_manifest and os.path.exists(bg_manifest):
            with open(bg_manifest) as f:
                all_bg = json.load(f)
            n_bg = int(len(self._digit_samples) * bg_ratio)
            rng  = random.Random(seed)
            rng.shuffle(all_bg)
            self._bg_paths = all_bg[:n_bg]
            print(f"  {len(self._digit_samples)} digit crops, {len(self._bg_paths)}/{len(all_bg)} bg crops (bg_ratio={bg_ratio})")
        else:
            print(f"  {len(self._digit_samples)} digit crops, no bg manifest provided")

        self._n_digits = len(self._digit_samples)

    def __len__(self):
        return self._n_digits + len(self._bg_paths)

    def __getitem__(self, idx):
        if idx < self._n_digits:
            img_path, label, top, left, h, w = self._digit_samples[idx]
            img = Image.open(img_path).convert("RGB")
            iw, ih = img.size
            x1 = max(0, int(left));      y1 = max(0, int(top))
            x2 = min(iw, int(left + w)); y2 = min(ih, int(top + h))
            crop = img.crop((x1, y1, x2, y2)) if x2 > x1 and y2 > y1 else Image.new("RGB", (4, 4), 128)
        else:
            crop  = Image.open(self._bg_paths[idx - self._n_digits]).convert("RGB")
            label = BG_LABEL

        if self.transform:
            crop = self.transform(crop)
        return crop, label


# ── Transforms ────────────────────────────────────────────────────────────────

def get_transforms(augment=False, cfg=None):
    if cfg is None:
        cfg = {}
    size = cfg.get("image_size", 32)
    mean = cfg.get("mean", [0.4377, 0.4438, 0.4728])
    std  = cfg.get("std",  [0.1980, 0.2010, 0.1970])
    base = [
        transforms.Resize((size, size)),
        transforms.ToTensor(),
        transforms.Normalize(mean=mean, std=std),
    ]
    if augment:
        aug = [
            transforms.RandomInvert(p=0.5),
            transforms.ColorJitter(
                brightness=cfg.get("color_jitter_brightness", 0.3),
                contrast=cfg.get("color_jitter_contrast", 0.3),
                saturation=cfg.get("color_jitter_saturation", 0.2),
            ),
            transforms.RandomAffine(
                degrees=cfg.get("augment_degrees", 15),
                translate=tuple(cfg.get("augment_translate", [0.1, 0.1])),
                scale=tuple(cfg.get("augment_scale", [0.9, 1.1])),
            ),
        ]
        return transforms.Compose(aug + base)
    return transforms.Compose(base)


# ── Split-aware dataset ───────────────────────────────────────────────────────

class SVHNSplitDataset(Dataset):
    """
    Loads digit crops for a specific list of images defined by a splits .txt file.
    Background crops loaded from pre-generated manifest (run scripts/generate_bg_crops.py first).
    """

    def __init__(self, data_dir, split_txt, transform=None, bg_manifest=None, bg_ratio=1.0, seed=42):
        self.transform      = transform
        self._digit_samples = []
        self._bg_paths      = []

        with open(split_txt) as f:
            rel_paths = [l.strip() for l in f if l.strip()]

        by_split = {}
        for rel in rel_paths:
            split_name, filename = rel.split("/", 1)
            by_split.setdefault(split_name, set()).add(filename)

        for split_name, filenames in by_split.items():
            split_dir = os.path.join(data_dir, split_name)
            records   = parse_digit_struct(os.path.join(split_dir, "digitStruct.mat"))
            for rec in tqdm(records, desc=f"Loading {split_name} crops", unit="img"):
                if rec["filename"] not in filenames:
                    continue
                img_path = os.path.join(split_dir, rec["filename"])
                if not os.path.exists(img_path):
                    continue
                for label, top, left, h, w in zip(
                    rec["labels"], rec["tops"], rec["lefts"],
                    rec["heights"], rec["widths"]
                ):
                    label = 0 if label == 10 else label
                    self._digit_samples.append((img_path, label, top, left, h, w))

        if bg_manifest and os.path.exists(bg_manifest):
            with open(bg_manifest) as f:
                all_bg = json.load(f)
            n_bg = int(len(self._digit_samples) * bg_ratio)
            rng  = random.Random(seed)
            rng.shuffle(all_bg)
            self._bg_paths = all_bg[:n_bg]
            print(f"  {len(self._digit_samples)} digit crops, {len(self._bg_paths)}/{len(all_bg)} bg crops (bg_ratio={bg_ratio})")
        else:
            print(f"  {len(self._digit_samples)} digit crops, no bg manifest provided")

        self._n_digits = len(self._digit_samples)

    def __len__(self):
        return self._n_digits + len(self._bg_paths)

    def __getitem__(self, idx):
        if idx < self._n_digits:
            img_path, label, top, left, h, w = self._digit_samples[idx]
            img = Image.open(img_path).convert("RGB")
            iw, ih = img.size
            x1 = max(0, int(left));      y1 = max(0, int(top))
            x2 = min(iw, int(left + w)); y2 = min(ih, int(top + h))
            crop = img.crop((x1, y1, x2, y2)) if x2 > x1 and y2 > y1 else Image.new("RGB", (4, 4), 128)
        else:
            crop  = Image.open(self._bg_paths[idx - self._n_digits]).convert("RGB")
            label = BG_LABEL
        if self.transform:
            crop = self.transform(crop)
        return crop, label


# ── DataLoaders ───────────────────────────────────────────────────────────────

def get_dataloaders(data_dir="data", batch_size=128, num_workers=2, cfg=None, use_extra=True):
    if cfg is None:
        cfg = {}
    ds_cfg      = cfg.get("dataset", cfg)
    num_workers = ds_cfg.get("num_workers", num_workers)
    splits_dir  = cfg.get("splits_dir", "data/splits")
    bg_crops_dir = cfg.get("bg_crops_dir", "data/bg_crops")

    train_txt = os.path.join(splits_dir, "train.txt")
    val_txt   = os.path.join(splits_dir, "val.txt")

    if not use_extra:
        import tempfile, atexit
        with open(train_txt) as f:
            lines = [l for l in f if not l.startswith("extra/")]
        tmp = tempfile.NamedTemporaryFile(mode="w", suffix=".txt", delete=False)
        tmp.writelines(lines)
        tmp.close()
        atexit.register(os.unlink, tmp.name)
        train_txt = tmp.name
        print(f"  [non-CUDA] Skipping extra/ — using {len(lines)} train/ images only")

    if not os.path.exists(train_txt) or not os.path.exists(val_txt):
        raise FileNotFoundError(
            f"Split files not found in {splits_dir}/. Run: python src/data/splits.py"
        )

    train_manifest = os.path.join(bg_crops_dir, "train_manifest.json")
    test_manifest  = os.path.join(bg_crops_dir, "test_manifest.json")

    if not os.path.exists(train_manifest):
        raise FileNotFoundError(
            f"BG crops manifest not found: {train_manifest}. Run: python scripts/generate_bg_crops.py"
        )

    train_ds = SVHNSplitDataset(
        data_dir, train_txt,
        transform=get_transforms(augment=True, cfg=ds_cfg),
        bg_manifest=train_manifest,
        bg_ratio=ds_cfg.get("bg_ratio", 1.0),
        seed=ds_cfg.get("seed", 42),
    )
    val_ds = SVHNSplitDataset(
        data_dir, val_txt,
        transform=get_transforms(augment=False, cfg=ds_cfg),
        bg_manifest=None,
    )
    test_ds = SVHNFormat1Dataset(
        os.path.join(data_dir, "test"),
        transform=get_transforms(augment=False, cfg=ds_cfg),
        bg_manifest=test_manifest if os.path.exists(test_manifest) else None,
        bg_ratio=ds_cfg.get("bg_ratio", 1.0),
        seed=ds_cfg.get("seed", 42),
    )

    persist = num_workers > 0
    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True,
                              num_workers=num_workers, pin_memory=True,
                              persistent_workers=persist)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size, shuffle=False,
                              num_workers=num_workers, pin_memory=True,
                              persistent_workers=persist)
    test_loader  = DataLoader(test_ds,  batch_size=batch_size, shuffle=False,
                              num_workers=num_workers, pin_memory=True,
                              persistent_workers=persist)

    return train_loader, val_loader, test_loader
